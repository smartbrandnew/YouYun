package com.broada.carrier.monitor.impl.ew.domino.basic46;

public class Domino46Exception extends Exception {
	private static final long serialVersionUID = 1L;

	public Domino46Exception() {
    super();
  }

  public Domino46Exception(String message) {
    super(message);
  }

  public Domino46Exception(Throwable cause) {
    super(cause);
  }

  public Domino46Exception(String message, Throwable cause) {
    super(message, cause);
  }

}
